package gicjp;

public class Relational {
    public static void main(String[] args) {
        int x=100;
        int y=100;
        System.out.println("The result for x=y  "+(x==y));
        System.out.println("The result for x!=y  "+(x!=y));
        System.out.println("The result for x=>y  "+(x>=y));
        System.out.println("The result for x<=y  "+(x<=y));
    }
}
