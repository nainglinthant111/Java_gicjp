package gicjp;
public class OperatorsDemo {
    public static void main(String[] args) {
        int a=20;
        int b = 17;
        double x=18.23;
        double y=15.17;
        System.out.println("a+b="+(a+b));
        System.out.println("x+y="+(x+y));
        System.out.println("a * x ="+(a*x));
        System.out.println("b/y="+(b/y));

        int c=(int) x;
        System.out.println("c ="+ c);
    }
}
